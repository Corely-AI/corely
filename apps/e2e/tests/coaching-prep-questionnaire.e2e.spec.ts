async function startBooking(
  request: import("@playwright/test").APIRequestContext,
  auth: ReturnType<typeof buildAuthHeaders>,
  offer: CoachingOfferDto,
  hold: { id: string; startAt: string; endAt: string },
  clientPartyId: string
) {
  if (!offer.coachUserId) {
    throw new Error("Offer did not include coachUserId");
  }
  const response = await request.post(`${API_URL}/coaching-engagements`, {
    headers: auth,
    data: {
      clientPartyId,
      bookingHoldId: hold.id,
      coachUserId: offer.coachUserId,
      locale: "en",
      offer: {
        title: offer.title,
        description: offer.description ?? undefined,
        currency: offer.currency,
        priceCents: offer.priceCents,
        sessionDurationMinutes: offer.sessionDurationMinutes,
        meetingType: offer.meetingType,
        availabilityRule: offer.availabilityRule,
        bookingRules: offer.bookingRules,
        contractRequired: offer.contractRequired,
        paymentRequired: offer.paymentRequired,
        localeDefault: offer.localeDefault,
        contractTemplate: offer.contractTemplate ?? null,
        contractLabel: offer.contractLabel ?? undefined,
        prepFormTemplate: offer.prepFormTemplate ?? null,
        prepFormSendHoursBeforeSession: offer.prepFormSendHoursBeforeSession ?? null,
        debriefTemplate: offer.debriefTemplate ?? null,
      },
      session: {
        startAt: hold.startAt,
        endAt: hold.endAt,
      },
    },
  });
  expect(response.ok(), await response.text()).toBeTruthy();
  return (await response.json()) as {
    engagement: { id: string; status: string };
    session: { id: string };
  };
}

async function runWorkerTick(request: import("@playwright/test").APIRequestContext) {
  const serviceToken = process.env.WORKER_API_SERVICE_TOKEN?.trim();
  const legacyWorkerKey = process.env.INTERNAL_WORKER_KEY?.trim();
  const headers: Record<string, string> = {
    "Content-Type": "application/json",
  };
  if (serviceToken) {
    headers["x-service-token"] = serviceToken;
  } else if (legacyWorkerKey) {
    headers["x-worker-key"] = legacyWorkerKey;
  }
  const response = await request.post(`${API_URL}/internal/background/outbox/run`, {
    headers,
    data: { limit: 50 },
  });
  expect(response.ok(), await response.text()).toBeTruthy();
  return (await response.json()) as Record<string, unknown>;
}

