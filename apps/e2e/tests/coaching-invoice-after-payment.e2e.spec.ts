async function waitForInvoicesBySource(
  tenantId: string,
  sourceId: string,
  expectedCount: number
): Promise<SourceInvoiceLookup[]> {
  const deadline = Date.now() + 60_000;
  while (Date.now() < deadline) {
    await apiClient.post("/test/drain-outbox");
    const body = await apiClient.post<{ invoices: SourceInvoiceLookup[] }>("/test/invoices/by-source", {
      tenantId,
      sourceType: "coaching_engagement",
      sourceId,
    });
    if (body.invoices.length >= expectedCount) {
      return body.invoices;
    }
    await new Promise((resolve) => setTimeout(resolve, 1_000));
  }
  throw new Error("Timed out waiting for coaching invoice");
}

async function waitForInvoiceDeliveries(
  tenantId: string,
  invoiceId: string,
  expectedCount: number
): Promise<InvoiceDelivery[]> {
  const deadline = Date.now() + 90_000;
  while (Date.now() < deadline) {
    await apiClient.post("/test/drain-outbox");
    const body = await apiClient.post<{ deliveries: InvoiceDelivery[] }>("/test/invoices/email-deliveries", {
      tenantId,
      invoiceIds: [invoiceId],
    });
    if (
      body.deliveries.length >= expectedCount &&
      body.deliveries[0] &&
      body.deliveries[0].status === "SENT" &&
      body.deliveries[0].providerMessageId
    ) {
      return body.deliveries;
    }
    await new Promise((resolve) => setTimeout(resolve, 1_000));
  }
  throw new Error("Timed out waiting for invoice email delivery");
}

async function waitForInvoicePdf(
  request: import("@playwright/test").APIRequestContext,
  auth: ReturnType<typeof buildAuthHeaders>,
  invoiceId: string
) {
  const deadline = Date.now() + 60_000;
  while (Date.now() < deadline) {
    await apiClient.post("/test/drain-outbox");
    const response = await request.get(`${API_URL}/invoices/${invoiceId}/pdf`, {
      headers: auth,
      params: { waitMs: "0" },
    });
    expect([200, 202].includes(response.status()), await response.text()).toBeTruthy();
    const body = (await response.json()) as {
      status: "PENDING" | "READY" | "FAILED";
      documentId?: string;
      downloadUrl?: string;
      errorMessage?: string;
    };
    if (body.status === "READY") {
      return body;
    }
    if (body.status === "FAILED") {
      throw new Error(body.errorMessage ?? "Invoice PDF generation failed");
    }
    await new Promise((resolve) => setTimeout(resolve, 1_000));
  }
  throw new Error("Timed out waiting for invoice PDF");
}

test.describe("Coaching invoice after payment", () => {
  test("generates one invoice, includes correct billing details, sends email, and resends without duplicate accounting rows", async ({
    page,
    request,
    testData,
  }, testInfo) => {
    const screenshotDir = screenshotDirFor(testInfo);
    const auth = buildAuthHeaders(await loginAsSeededUser(request, testData));
    const suffix = uniqueSuffix();
    const timezone = "Europe/Berlin";
    const clientName = `Invoice Client ${suffix}`;
    const clientEmail = `delivered+coaching-invoice-${suffix}@resend.dev`;

    const offer = await createOffer(request, auth, timezone, suffix);
    const localStartDate = nextLocalWeekday(1, timezone);
    const localEndDate = plusLocalDays(localStartDate, 1);
    const availability = await getAvailability(request, offer.id, {
      from: `${localStartDate}T00:00:00.000Z`,
      to: `${localEndDate}T23:59:59.999Z`,
      timezone,
    });
    const slot = availability.slots[0];
    expect(slot).toBeTruthy();

    const hold = await createHold(request, offer.id, slot, clientName, clientEmail);
    const booking = await startBooking(request, offer.id, hold.hold.id, clientName, clientEmail);

    await captureState(page, testInfo, screenshotDir, "01-booking-started", "Booking started", {
      offerId: offer.id,
      engagementId: booking.engagement.id,
      paymentId: booking.payment.id,
      sessionId: booking.session.id,
      checkoutUrl: booking.checkoutUrl,
    });

    await postStripeWebhook(request, {
      id: `evt_invoice_capture_${suffix}`,
      type: "checkout.session.completed",
      data: {
        object: {
          id: booking.payment.providerCheckoutSessionId,
          metadata: {
            tenantId: testData.tenant.id,
            engagementId: booking.engagement.id,
            paymentId: booking.payment.id,
          },
          payment_intent: `pi_invoice_${suffix}`,
          payment_status: "paid",
        },
      },
    });

    const invoicesBySource = await waitForInvoicesBySource(
      testData.workspace.id,
      booking.engagement.id,
      1
    );
    expect(invoicesBySource).toHaveLength(1);
    expect(invoicesBySource[0]?.paymentCount).toBe(1);

    const invoiceId = invoicesBySource[0]!.invoiceId;
    const invoiceResponse = await getInvoice(request, auth, invoiceId);
    const invoice = invoiceResponse.invoice;
    expect(invoice.sourceType).toBe("coaching_engagement");
    expect(invoice.sourceId).toBe(booking.engagement.id);
    expect(invoice.currency).toBe("EUR");
    expect(invoice.billToName).toBe(clientName);
    expect(invoice.billToEmail).toBe(clientEmail);
    expect(invoice.lineItems).toHaveLength(1);
    expect(invoice.lineItems[0]?.unitPriceCents).toBe(offer.priceCents);
    expect(invoice.totals.subtotalCents).toBe(offer.priceCents);
    expect(invoice.totals.taxCents).toBe(
      typeof invoice.taxSnapshot?.taxTotalAmountCents === "number"
        ? invoice.taxSnapshot.taxTotalAmountCents
        : 0
    );

    const pdf = await waitForInvoicePdf(request, auth, invoiceId);
    await captureState(page, testInfo, screenshotDir, "02-invoice-generated", "Invoice generated", {
      invoiceSummary: invoicesBySource[0],
      invoice,
      pdf,
    });

    const initialDeliveries = await waitForInvoiceDeliveries(testData.workspace.id, invoiceId, 1);
    expect(initialDeliveries).toHaveLength(1);
    await captureState(page, testInfo, screenshotDir, "03-email-sent", "Initial invoice email sent", {
      invoiceId,
      deliveries: initialDeliveries,
    });

    const resendResponse = await request.post(
      `${API_URL}/coaching-engagements/${booking.engagement.id}/invoice/resend`,
      {
        headers: auth,
        data: {},
      }
    );
    expect(resendResponse.ok(), await resendResponse.text()).toBeTruthy();
    const resent = (await resendResponse.json()) as {
      invoiceId: string;
      deliveryId: string;
      status: string;
      to: string;
    };
    expect(resent.invoiceId).toBe(invoiceId);
    expect(resent.to).toBe(clientEmail);

    const deliveriesAfterResend = await waitForInvoiceDeliveries(
      testData.workspace.id,
      invoiceId,
      2
    );
    const invoicesAfterResend = await waitForInvoicesBySource(
      testData.workspace.id,
      booking.engagement.id,
      1
    );
    expect(invoicesAfterResend).toHaveLength(1);
    expect(invoicesAfterResend[0]?.paymentCount).toBe(1);
    expect(invoicesAfterResend[0]?.deliveryCount).toBe(2);

    await captureState(
      page,
      testInfo,
      screenshotDir,
      "04-resend-without-duplicates",
      "Invoice resent without duplicate accounting rows",
      {
        resent,
        invoicesAfterResend,
        deliveriesAfterResend,
      }
    );
  });
});
